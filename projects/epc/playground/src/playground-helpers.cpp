std::string to_string(double d)
{
  std::stringstream ss;
  ss << std::setprecision(std::numeric_limits<double>::max_digits10) << d;
  return ss.str();
}

std::ostream& wirteIntToStream(std::ostream& stream, int i)
{
  return stream << i;
}

